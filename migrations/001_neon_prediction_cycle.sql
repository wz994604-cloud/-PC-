BEGIN;

CREATE TABLE IF NOT EXISTS draw_results (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  issue TEXT NOT NULL UNIQUE,
  num1 SMALLINT NOT NULL CHECK (num1 BETWEEN 0 AND 9),
  num2 SMALLINT NOT NULL CHECK (num2 BETWEEN 0 AND 9),
  num3 SMALLINT NOT NULL CHECK (num3 BETWEEN 0 AND 9),
  sum SMALLINT NOT NULL CHECK (sum BETWEEN 0 AND 27),
  big_small TEXT NOT NULL CHECK (
    (sum BETWEEN 0 AND 13 AND big_small = '小') OR
    (sum BETWEEN 14 AND 27 AND big_small = '大')
  ),
  odd_even TEXT NOT NULL CHECK (
    (MOD(sum, 2) = 0 AND odd_even = '双') OR
    (MOD(sum, 2) = 1 AND odd_even = '单')
  ),
  pattern TEXT NOT NULL,
  open_time TIMESTAMPTZ,
  source TEXT NOT NULL DEFAULT 'jnd',
  raw_data JSONB,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS draw_results_issue_desc
  ON draw_results ((issue::BIGINT) DESC);

CREATE TABLE IF NOT EXISTS prediction_records (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  target_issue TEXT NOT NULL UNIQUE,
  based_on_issue TEXT NOT NULL,
  model_version TEXT NOT NULL DEFAULT 'v0.1',
  recommended_sum SMALLINT NOT NULL CHECK (recommended_sum BETWEEN 0 AND 27),
  top3 JSONB NOT NULL,
  top5 JSONB NOT NULL,
  score SMALLINT NOT NULL,
  confidence SMALLINT NOT NULL,
  confidence_label TEXT NOT NULL,
  risk_level TEXT NOT NULL,
  sample_size INTEGER NOT NULL,
  weights JSONB NOT NULL,
  probability_distribution JSONB NOT NULL,
  status TEXT NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'settled')),
  actual_sum SMALLINT CHECK (actual_sum BETWEEN 0 AND 27),
  is_hit BOOLEAN,
  generated_at TIMESTAMPTZ NOT NULL,
  settled_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS prediction_records_target_issue_desc
  ON prediction_records ((target_issue::BIGINT) DESC);
CREATE INDEX IF NOT EXISTS prediction_records_status
  ON prediction_records (status);

COMMIT;
